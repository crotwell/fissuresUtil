package edu.sc.seis.fissuresUtil.chooser;


public class ChannelChooserException extends Exception {

    public ChannelChooserException() {
    }

    public ChannelChooserException(String arg0) {
        super(arg0);
    }

    public ChannelChooserException(Throwable arg0) {
        super(arg0);
    }

    public ChannelChooserException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
